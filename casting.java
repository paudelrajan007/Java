public class casting{
public static void main(String[] args) {
    // Implicit Casting 
    double price=100.10;
    double finalp=price+18.00;
    System.out.println("The final price"+finalp);
    // Explicit Casting
    int p=10;
    int fp=p+(int)10.0;
    System.out.println(".(final p)"+fp);
}
}